// ! ======================== Array Methods ===============================

var ar = [10,20,30,40,50,60,70,80]
console.log(ar)

// ? pop():
// It will remove the element from the last index.
// Syntax : arrayName.pop();
ar.pop()
console.log(ar)

// ? push()
// it will add the element at the index 
// syntax : arrayName.push(element);

ar.push(800)
console.log(ar)

// ? shift()
//  it will remove the element from the 0th index 
// Syntax: arrayName.shift()

ar.shift()
console.log(ar)

// ? unshift()
//  it will  add the element at the 0th index 
// Syntax: arrayName.shift()

ar.unshift(100)
console.log(ar)

// ? slice
//  it will extract the subpart of an array
// Syntax: arrayName.slice(SI , EI )  starting index & ending index
